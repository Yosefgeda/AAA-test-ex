const calculator = {

    add: add = (a, b) => {
        return a + b;
    },

    subtract: subtract = (a, b) => {
        return a - b;
    },

    divide: divide = (a, b) => {
        return a/b;
    },
    
    multiply: multiply = (a, b) => {
        return a * b;
    },
};


 module.exports = calculator;